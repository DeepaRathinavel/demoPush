package week1day1;

public class RelationalOperators {
	static int n1=20, n2=10;
	static void Operators() {
		System.out.println("Greatest Number :"+(n1>n2));
		System.out.println("Smallest Number :"+(n1<n2));
		System.out.println("Equal :"+(n1==n2));
		System.out.println("Not Equal :"+(n1!=n2));
		System.out.println("N1 GreaterThanOREqueal :"+(n1>=n2));
		System.out.println("N2 GreaterThanOREqueal :"+(n2<=n1));
	}

	public static void main(String args[])
	{
     Operators();
	}
}
